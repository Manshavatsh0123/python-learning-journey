
#Class attribute
class Employee:
    language = "py"
    salary=1200000


mansha = Employee()
mansha.name="Mansha Vatsh" #Object attribute
print(mansha.language,mansha.salary,mansha.name) #language and salary is class attribute


mohit = Employee()
mohit.name="Mohit Raj"#Object attribute
print(mohit.language,mohit.salary,mohit.name) #language and salary is class attribute


